public class NewNumber {
    private String name;
    private String number;

    public NewNumber(){
        this.name = "";
        this.number = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
